-------
select Apellido +''+ Nombre 'Médico'
from UsuarioMedico
where Localidad like 'C%' and DniM < 20000000
-------
select IdEm ID, Tipo, Fecha, Paciente, MedicoSoliciatante
from EstudiosMedicos
where year (Fecha) = 2022 and year (Fecha) = year (getdate())
-------
select Paciente
from EstudiosMedicos
where Tipo = 'Radiografía' and Tipo = 'Traumatología'
-------
select em.IdEm 'ID', Tipo, Apellido +''+ Nombre Paciente 
from EstudiosMedicos em, UsuarioPaciente up
where em.Paciente = up.DniU
    and em.IdEm in (1, 3, 5, 7) and up.ObraSocial like 'O%'  
-------
